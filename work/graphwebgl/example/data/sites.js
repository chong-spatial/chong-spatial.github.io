export const sitesTypes = ['User', 'Location', 'Transaction'];

const linkTypes = ['leng', 'l', 'lengt', 'le', 'len', 'length'];
export default function sitesGraphData() {
  const nodeTypeMap = new Map();
  const graph = new Graph('sites');
  for (const n of sitesRaw.nodes) {
    const nodeType = sitesTypes[Math.floor(Math.random() * sitesTypes.length)];
    graph.addNode({id: n.id, type: nodeType});
    nodeTypeMap.set(n.id, nodeType);
  }
  for (const l of sitesRaw.links) {
    graph.addLink({
      fromId: l.source,
      fromType: nodeTypeMap.get(l.source),
      toId: l.target,
      toType: nodeTypeMap.get(l.target),
      type: linkTypes[Math.floor(Math.random() * linkTypes.length)],
      discriminator: '',
      directed: true
  });
  }

  return graph;
}
